export const createWorld = (matrix: number[][]) => {
  const elements = [];

  matrix.forEach((row, indexY) => {
    row.forEach((floor, indexX) => {
      if (myClass[floor]) {
        const className = myClass[floor];
        const width = 32;
        const height = 32;

        elements.push(new className(indexX * width, indexY * height));
      }
    });
  });
  return elements;
};
